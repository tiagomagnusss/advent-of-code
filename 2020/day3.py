	def countTrees(self, inputs: list) -> int:
		trees = 0
		x, y = 0,0

		while y < len(inputs)-1:
			x += 3
			y += 1

			while x > len(inputs[y]):
				inputs[y] += inputs[y]

			if inputs[y][x] == 1:
				trees += 1

		return trees

